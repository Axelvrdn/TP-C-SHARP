namespace FonctionPartie2
{
    public class Class1Base
    {
        public string DonneMois(int mois)
        {
            if (mois <= 12)
            {
                if (mois > 0)
                {
                    if (mois == 1)
                    {
                        return "Janvier";
                    }
                    if (mois == 2)
                    {
                        return "Fevrier";
                    }
                    if (mois == 3)
                    {
                        return "Mars";
                    }
                    if (mois == 4)
                    {
                        return "Avril";
                    }
                    if (mois == 5)
                    {
                        return "Mai";
                    }
                    if (mois == 6)
                    {
                        return "Juin";
                    }
                    if (mois == 7)
                    {
                        return "Juillet";
                    }
                    if (mois == 8)
                    {
                        return "Aout";
                    }
                    if (mois == 9)
                    {
                        return "Septembre";
                    }
                    if (mois == 10)
                    {
                        return "Octobre";
                    }
                    if (mois == 11)
                    {
                        return "Novembre";
                    }
                    if (mois == 12)
                    {
                        return "Décembre";
                    }

                }
            }
        }
    }
}